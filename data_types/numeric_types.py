# Integer and float are the two main numeric types in Python.
# Integers are whole numbers, while floats are numbers with decimal points.
# Example of an integer
Int_Number = 42
print(Int_Number)           # Output: 42    
# Example of a float
Float_Number = 3.14
print(Float_Number)         # Output: 3.14  
# Example of a complex number
Complex_Number = 2 + 3j
print(Complex_Number)       # Output: (2+3j)
# Example of a decimal number
from decimal import Decimal
Decimal_Number = Decimal('2.71828')
print(Decimal_Number)       # Output: 2.71828
# Example of a fraction
from fractions import Fraction
Fraction_Number = Fraction(1, 3)
print(Fraction_Number)      # Output: 1/3